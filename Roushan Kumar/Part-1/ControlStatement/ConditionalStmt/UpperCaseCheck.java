//WAJP to check the Character is in uppercase char or not

class UpperCaseCheck
{
	public static void main(String []args)
	{	
		char ch='9';
		if((ch>=65 && ch<=90))
		{
			System.out.println("Yes! The character is in UpperCase.");
		}else{
		
			System.out.println("No! It is not in upperCase.");
		     }
	}
}