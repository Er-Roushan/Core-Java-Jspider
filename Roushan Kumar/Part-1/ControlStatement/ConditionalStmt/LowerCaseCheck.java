//WAJP to check the Character is in LowerCase char 

class LowerCaseCheck
{
	public static void main(String []args)
	{	
		char ch='N';
		if((ch>=97 && ch<=122))
		{
			System.out.println("Yes! The character is in LowerCase.");
		}else {
			System.out.println("No! The character is not in lowercase.");
		      }
	}
}