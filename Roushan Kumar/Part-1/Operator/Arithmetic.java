class Arithmetic 
{
	public static void main(String[] args)
	{
		System.out.println(53.5/2);
		System.out.println(53.5%2);

		System.out.println(54.0/2);
		System.out.println(54.0%2);

		System.out.println(97/2.0);
		System.out.println(97%2.0);

		System.out.println(32.0/2);
		System.out.println(32.0%2.0);

		System.out.println(38.0/2.5);
		System.out.println(38.0%2.5);

		System.out.println("====================");

		System.out.println(10/5);
		System.out.println(10%5);

		System.out.println(15/2);
		System.out.println(15%2);

	}
}