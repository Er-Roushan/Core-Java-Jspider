class Decrement
{
	public static void main(String []args)
	{
		int p=3;
		System.out.println(--p);

		System.out.println("===========");

		int q=4;
		System.out.println(--q);

		System.out.println("===========");

		int x=150;
		System.out.println(--x);

		System.out.println("===========");

		int z=-3;
		System.out.println(--z);

		System.out.println("===========");

		int y=-7;
		System.out.println(--y);

	}
}